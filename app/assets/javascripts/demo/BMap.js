function openDis(){
    var myDis = new BMapLib.DistanceTool(map);
    map.addEventListener("load",function(){
        myDis.open();  //开启鼠标测距
    });
}
function closeDis(){
    var myDis = new BMapLib.DistanceTool(map);
    map.addEventListener("load",function(){
        myDis.close();  //关闭鼠标测距
    });
}


